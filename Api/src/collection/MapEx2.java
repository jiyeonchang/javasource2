package collection;

import java.util.Collection;
import java.util.HashMap;
import java.util.Iterator;
import java.util.Map;
import java.util.Map.Entry;
import java.util.Set;
//map으로 전체적으로 값 가져오기 1
public class MapEx2 {

	public static void main(String[] args) {
		Map<String, String> map = new HashMap<String, String>();
		// 요소 추가
		map.put("id_111", "홍길동 ");
		map.put("id_222", "장길동 ");
		map.put("id_333", "최길동 ");
		map.put("id_444", "락길동 ");

		//가져오기 
		Set<String >keys = map.keySet();//중간정도 구조 
		//List ,set ,map에 담겨진 테이터들을 표준화된 방법으로 가져올 수 있도록 제공 
		Iterator<String> iter = keys.iterator()	;
		while (iter.hasNext()) {//hasNext가져올 테이터가있냐?라는의미next()가 가져와줌 
//			System.out.println(iter.next());
			String userId = iter.next()	;
			String value = map.get(userId);
			System.out.println(userId +" : "+value);
			
		}
	System.out.println();
	//두번째 방법 
	
	//Map.Entry 객체 사용 
	Set<Entry<String, String>> entrySet = map.entrySet();
	Iterator<Entry<String, String>> iter2= entrySet.iterator();
	
	while(iter.hasNext()) {
		Entry<String, String> entry = iter2.next();
		System.out.println(entry.getKey()+" : "+entry.getValue());
	}
	}

}
